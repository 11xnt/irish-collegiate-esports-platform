import Head from 'next/head'
import Image from 'next/image'
import Link from 'next/link'
import { Inter } from '@next/font/google'
import styles from '../styles/Home.module.css'
import Menu from '../components/menu'
import OrganiserList from '../components/organiserList'
import useSWR	from 'swr';
import { useEffect, useState } from 'react'
import { useRouter } from 'next/router'
import { useSession } from 'next-auth/react'
import OrganiserForm from '../components/forms/organiser'
const inter = Inter({ subsets: ['latin'] })

const fetcher = (...args: [any, any]) => fetch(...args).then((res) => res.json())


export default function Organisers(props) {

  const router = useRouter()
  const { data: session, status } = useSession()
  const [calledPush, setCalledPush] = useState(false)
  const [foundTeams, setFoundTeams] = useState([])
  const [isDisplay, setDisplay] = useState(false)

  useEffect(()=>{
    if(status !== "loading"){
      if (status === "authenticated") {
        if(calledPush) return
        else{
          router.push('/organisers')
          setCalledPush(true)
        }
      }else{
        router.push('/')
        return setCalledPush(true)
      }
  }},[router,session])

  const { data, error } = useSWR('/api/organisers/all', fetcher);

  if (error) return <div>Failed to load</div>
  if (!data) return <div>Loading...</div>
  if(data) {
    return (
      <>
        <Head>
          <title>Organisers Page</title>
          <meta name="description" content="Generated by create next app" />
          <meta name="viewport" content="width=device-width, initial-scale=1" />
          <link rel="icon" href="/favicon.ico" />
        </Head>
        <main>
        <Menu/>
          <div className={styles.container}>
          {/* row */}
          <div className={`${styles.containerItem} ${styles.containerItem3}`}>
              <h2>Tournament Organisers</h2>
              <button onClick={() => setDisplay(!isDisplay)}>Create a Tournament Organiser</button>
              {isDisplay ? <OrganiserForm user={session.user.email}/> : null}
              <div className={styles.cardRow}>
                {
                  data.length > 0 ? <OrganiserList organisers={data}/> : <h2>No Tournament Organisers Found</h2>
                }
              </div>
          </div>
        </div>
        </main>
      </>
    )
  }
}

